<script>
import { ref } from "vue";
import requisitionsServices from "../../services/requisitionsServices";
import PageHeader from '../../components/TopHeader.vue';
import WelcomeHead from '../../components/PageMenuHeader.vue';
import { useCounterStore } from "../../store/piniaStore"
import pinia from "../../store/store";
import administrationsService from "../../services/administrationsService";

const stores = useCounterStore(pinia);

export default {
  components: {
    PageHeader, WelcomeHead
  },
  props: {
    size: {
      type: Number,
      required: false,
      default: 15
    }
  },
  data() {
    return {
      showResults: ref(false),
      showResults2: ref('myRecords'),
      ascending: true,
      sortBy: 'alphabetically',
      newStatus: 0,
      awaitingApprovalStatus: 0,
      approvedStatus: 0,
      inProgressStatus: 0,
      contracts: [],
      completedStatus: 0,
      pendingRatingStatus: 0,
      searchValue: '',
      pageNumber: 0,  // default to page 0
      currentDate: new Date().toJSON().slice(0, 10).replace(/-/g, '-'),
      values2: [{ "newactivityText": "New" }, { "newactivityText": "Awaiting Approval" },
      { "newactivityText": "Approved" }, { "newactivityText": "In Progress" },
      { "newactivityText": "Completed" }, { "newactivityText": "Pending Rating" }],
      values: [],
      valuesAllRecords: []
    };
  },

  async created() {
    // this.valuesAllRecords = await requisitionsServices.getRequisitions();
    // if (stores.signedInUserRole == '') {
    //   //  stores.signedInUserRole =  (await administrationsService.getLoggedInUserRoles('samuel.oduniyi@codeware.com.ng')).roleName
    //   //use this for production, remove above
    //   stores.signedInUserRole = (await administrationsService.getLoggedInUserRoles(stores.signedInUserEmail)).roleName
    // }
  },

  computed: {
    filteredRFQs() {
       this.values  = this.valuesAllRecords.filter(o=>o.requestedBy === stores.signedInUserEmail) 
      if (this.showResults2 == 'allRecords') {
        this.newStatus = this.valuesAllRecords.filter(item => item.status === 'Open').length;
        this.awaitingApprovalStatus = this.valuesAllRecords.filter(item => item.status === 'Awaiting Approval').length;
        this.approvedStatus = this.valuesAllRecords.filter(item => item.status === 'Approved').length;
        this.inProgressStatus = this.valuesAllRecords.filter(item => item.status === 'In Progress').length;
        this.completedStatus = this.valuesAllRecords.filter(item => item.status === 'Completed').length;
        this.pendingRatingStatus = this.valuesAllRecords.filter(item => item.status === 'Pending Rating').length;
      }

      else if (this.showResults2 == 'myRecords') {
        this.newStatus = this.values.filter(item => item.status === 'Open').length;
        this.awaitingApprovalStatus = this.values.filter(item => item.status === 'Awaiting Approval').length;
        this.approvedStatus = this.values.filter(item => item.status === 'Approved').length;
        this.inProgressStatus = this.values.filter(item => item.status === 'In Progress').length;
        this.completedStatus = this.values.filter(item => item.status === 'Completed').length;
        this.pendingRatingStatus = this.values.filter(item => item.status === 'Pending Rating').length;
      }
      const start = this.pageNumber * this.size,
        end = start + this.size;
      if (this.showResults2 == 'allRecords') {
        this.contracts = this.valuesAllRecords
      }
      else if (this.showResults2 == 'myRecords') {
        this.contracts = this.values

      }
      // Process search input
      if (this.searchValue != '' && this.searchValue) {
        this.contracts = this.contracts.filter((item) => {
          return item.status
            .toLowerCase()
            .includes(this.searchValue.toLowerCase())
        })
      }

      // Sort by alphabetical order
      this.contracts = this.contracts.sort((a, b) => {
        if (this.sortBy == 'alphabetically') {
          let fa = a.id.toLowerCase(), fb = b.id.toLowerCase()

          if (fa < fb) {
            return 1
          }
          if (fa > fb) {
            return -1
          }
          return 0

        }
      })

      // Show sorted array in descending or ascending order
      if (!this.ascending) {
        this.contracts.reverse().slice(start, end)
      }

      return this.contracts.slice(start, end)
    },
  },

  methods: {
    filterbyStatus(id) {
      this.searchValue = id
    },

    onButtonSelected(isCorrects) {
      if (isCorrects) {
        this.showResults2 = isCorrects;
        return this.showResults2
      }
    },
    navigateToRFQ(id) {
      stores.rfqNo = id
      this.$router.push(`/rfqedit/${id}`)
    }
  }


};
</script>

